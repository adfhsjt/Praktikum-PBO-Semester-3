    private void tanggalLahirFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tanggalLahirFocusLost
        // TODO add your handling code here:
        try {
            JFormattedTextField field = (JFormattedTextField) evt.getSource();
            String dateText = field.getText();

            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            Date birthDate = sdf.parse(dateText);

            Calendar birthCal = Calendar.getInstance();
            birthCal.setTime(birthDate);

            Calendar today = Calendar.getInstance();

            int age = today.get(Calendar.YEAR) - birthCal.get(Calendar.YEAR);

            if (today.get(Calendar.MONTH) < birthCal.get(Calendar.MONTH)
                    || (today.get(Calendar.MONTH) == birthCal.get(Calendar.MONTH)
                    && today.get(Calendar.DAY_OF_MONTH) < birthCal.get(Calendar.DAY_OF_MONTH))) {
                age--;
            }

            if (age < 18) {
                JOptionPane.showMessageDialog(null,
                        "Anda belum cukup umur untuk melamar pekerjaan ini",
                        "Peringatan",
                        JOptionPane.WARNING_MESSAGE);

                field.setText("");
            }

        } catch (ParseException e) {
        }
    }//GEN-LAST:event_tanggalLahirFocusLost

    private void IPKFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_IPKFocusLost
        // TODO add your handling code here:
        try {
            JFormattedTextField field = (JFormattedTextField) evt.getSource();
            String ipkText = field.getText();

            double ipk = Double.parseDouble(ipkText);

            String pendidikan = (String) jComboBox1.getSelectedItem();

            if (pendidikan.equals("SD") || pendidikan.equals("SMP") || pendidikan.equals("SMA")) {
                field.setText("");
            }

            if (ipk < 0.00 || ipk > 4.00) {
                JOptionPane.showMessageDialog(null,
                        "IPK tidak valid.",
                        "Peringatan",
                        JOptionPane.WARNING_MESSAGE);
                field.setText("");
            }
        } catch (NumberFormatException e) {
            ((JFormattedTextField) evt.getSource()).setText("");
        }
    }//GEN-LAST:event_IPKFocusLost

    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        String pendidikan = (String) jComboBox1.getSelectedItem();

        // Cek apakah pilihan adalah SD, SMP, atau SMA
        if (pendidikan.equals("SD") || pendidikan.equals("SMP") || pendidikan.equals("SMA")) {
            // Cek apakah IPK sudah terisi
            if (!IPK.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null,
                        "Kosongkan Nilai IPK, atau pilih pendidikan D1 ke atas.",
                        "Peringatan",
                        JOptionPane.WARNING_MESSAGE);
                IPK.setText(""); // Kosongkan nilai IPK
            }
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

