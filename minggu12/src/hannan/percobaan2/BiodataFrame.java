    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if (namaText.getText().isEmpty()) {
            sb.append("Nama tidak boleh kosong.\n");
        }
        if (nikText.getText().isEmpty()) {
            sb.append("NIK tidak boleh kosong.\n");
        }
        if (alamatText.getText().isEmpty()) {
            sb.append("Alamat tidak boleh kosong.\n");
        }
        if (telp.getText().isEmpty()) {
            sb.append("No. Telepon tidak boleh kosong.\n");
        }
        if (genderGroup.getSelection() == null) {
            sb.append("Pilih jenis kelamin.\n");
        }
        if (tanggalLahir.getText().isEmpty()) {
            sb.append("Tanggal lahir tidak boleh kosong.\n");
        }
        String pendidikan = (String) jComboBox1.getSelectedItem();
        if (!(pendidikan.equals("SD") || pendidikan.equals("SMP") || pendidikan.equals("SMA"))) {
            if (IPK.getText().isEmpty()) {
                sb.append("IPK tidak boleh kosong.\nAtau pilih pendidikan dibawah D1\n");
            }
        }

        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(null, sb.toString(), "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Anda berhasil mendaftar", "Pendaftaran Berhasil", JOptionPane.INFORMATION_MESSAGE);

            namaText.setText("");
            nikText.setText("");
            tanggalLahir.setText("");
            IPK.setText("");
            alamatText.setText("");
            bacaCheckBox.setSelected(false);
            makanCheckBox.setSelected(false);
            tidurCheckBox.setSelected(false);
            genderGroup.clearSelection();
            jComboBox1.setSelectedIndex(0); // Atau set to default jika diperlukan
            telp.setText("");
        }
    }//GEN-LAST:event_okButtonActionPerformed

